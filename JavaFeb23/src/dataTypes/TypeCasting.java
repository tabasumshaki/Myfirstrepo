package dataTypes;

public class TypeCasting {

	public static void main(String[] args) {
		// Type casting is the process of converting 
		// one data types into another data type
		
		double mydouble = 56.98;
		int myint =(int)mydouble;
		
		System.out.println("double value is : " + mydouble);// double value  56.98
		// type casting into int value
		System.out.println("double value is : " + myint); // ? 56
		

	}

}
